#include "libft.h"

char	*ft_strcpyc(char *dest, const char *src, int c)
{
	int i;

	i = 0;
	while (*src && *src != c)
	{
		dest[i] = *src;
		src++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
