#include "libft.h"

void	ft_2print(char **tab, int bn)
{
	int i;

	i = 0;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		if (bn == 1)
			ft_putchar('\n');
		i++;
	}
}
