#include "libft.h"

int	ft_2compte_c(char **tab, char c)
{
	int y;
	int x;
	int nb;

	nb = 0;
	y = 0;
	while (tab[y])
	{
		x = 0;
		while (tab[y][x])
		{
			if (tab[y][x] == c)
				nb++;
			x++;
		}
		y++;
	}
	return (nb);
}
